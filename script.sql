CREATE OR REPLACE
FUNCTION POKAZIMIE(VID_ZWIERZECIA NUMBER)
    RETURN VARCHAR
IS
    VIMIE VARCHAR(30000);
    BEGIN
        SELECT IMIE
        INTO VIMIE
        FROM ZWIERZETA
        WHERE IDENTYFIKATOR_ZWIERZECIA=VID_ZWIERZECIA;

        RETURN VIMIE;
    END POKAZIMIE;
	
/
CREATE OR REPLACE
PROCEDURE ZMIENIMIE(VID_ZWIERZECIA NUMBER, VNOWE_IMIE VARCHAR)
IS
    BEGIN
        UPDATE ZWIERZETA
        SET IMIE = VNOWE_IMIE
        WHERE IDENTYFIKATOR_ZWIERZECIA=VID_ZWIERZECIA;

    END ZMIENIMIE;

